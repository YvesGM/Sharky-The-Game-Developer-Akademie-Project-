export default class Keyboard {

    /**
     * Stores the left movement state.
     *
     * @type {boolean}
     */
    LEFT = false;

    /**
     * Stores the right movement state.
     *
     * @type {boolean}
     */
    RIGHT = false;

    /**
     * Stores the up movement state.
     *
     * @type {boolean}
     */
    UP = false;

    /**
     * Stores the down movement state.
     *
     * @type {boolean}
     */
    DOWN = false;

    /**
     * Stores the space key state.
     *
     * @type {boolean}
     */
    SPACE = false;

    /**
     * Stores the D key state.
     *
     * @type {boolean}
     */
    D = false;

    /**
     * Stores the restart key state.
     *
     * @type {boolean}
     */
    RESTART = false;
}